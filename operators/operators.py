num_1=10
num_2=3


print(f'add : {num_1 + num_2}')
print(f'subtract : {num_1 - num_2}')
print(f'multiply : {num_1 * num_2}')
print(f'divide   : {num_1 / num_2}')
print(f'modulus  : {num_1 % num_2}')
print(f'power    : {num_1 ** num_2}')
print(f'floor div: {num_1 // num_2}')
print(f'unary plus     : {+num_1}')
print(f'unary minus    : {-num_1}')

# Comparison
print(f'equal          : {num_1 == num_2}')
print(f'not equal      : {num_1 != num_2}')
print(f'greater than   : {num_1 > num_2}')
print(f'less than      : {num_1 < num_2}')
print(f'greater or eq  : {num_1 >= num_2}')
print(f'less or eq     : {num_1 <= num_2}')

# Bitwise
print(f'bitwise AND    : {num_1 & num_2}')
print(f'bitwise OR     : {num_1 | num_2}')
print(f'bitwise XOR    : {num_1 ^ num_2}')
print(f'bitwise NOT    : {~num_1}')
print(f'left shift     : {num_1 << 1}')
print(f'right shift    : {num_1 >> 1}')